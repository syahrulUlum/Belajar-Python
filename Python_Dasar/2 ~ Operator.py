angka_pertama = 10
angka_kedua = 20

# Penambahan
print(angka_pertama + angka_kedua)

# Pengurangan
print(angka_pertama - angka_kedua)

# Perkalian
print(angka_pertama * angka_kedua)

# Pembagian
print(angka_pertama / angka_kedua)

# Sisa bagi hasil / modulo
print(angka_pertama % angka_kedua)